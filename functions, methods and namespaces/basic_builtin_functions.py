list1 = [5, 4, 3, 2, 1]  # a sample list

set1 = {600, 500, 400, 300, 200, 100}  # a sample set (unordered, no duplicates)

dict1 = {'a': 1, 'b': 2, 'c': 3}  # a sample dictionary with keys and values

len(list1)  # returns number of elements in list1

len(set1)  # returns number of elements in set1

len(dict1)  # returns number of key-value pairs in dict1

max(list1)  # returns the maximum value in list1

max(set1)  # returns the maximum value in set1

max(dict1)  # returns the maximum key in dict1 (compares keys alphabetically)

sorted(list1)  # returns sorted list1 without changing original list

sorted(set1)  # returns sorted version of set1 as a list

3 in list1  # checks if 3 is in list1 → returns True

3 in set1  # checks if 3 is in set1 → returns False

3 in dict1  # checks if 3 is a key in dict1 → returns False

'c' in dict1  # checks if 'c' is a key in dict1 → returns True


